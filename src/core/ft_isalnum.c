/* ft_isalnum
 * Checks if a char is alpha numeric
 *
 * c - The char to check
 */
int	ft_isalnum(int c)
{
	return ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z')
		|| (c >= '0' && c <= '9'));
}
